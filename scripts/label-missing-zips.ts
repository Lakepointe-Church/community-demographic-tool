async function getLabel(zip: string): Promise<string> {
  try {
    const res = await fetch(`https://api.zippopotam.us/us/${zip}`)
    if (!res.ok) return zip
    const json = await res.json() as { places?: { 'place name': string; state: string }[] }
    const place = json.places?.[0]
    return place ? `${place['place name']}` : zip
  } catch { return zip }
}

async function main() {
  const results: { zip: string; label: string }[] = []
  let i = 0
  for (const zip of MISSING) {
    const label = await getLabel(zip)
    results.push({ zip, label })
    i++
    if (i % 20 === 0) process.stdout.write(`${i}/${MISSING.length}... `)
  }
  console.log('\n')
  for (const { zip, label } of results) {
    console.log(`{ zip: '${zip}', label: '${label}' },`)
  }
}

main().catch(err => { console.error(err); process.exit(1) })
